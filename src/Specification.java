public class Specification {

    private String id;

    private String urlId;

    private String brand;

    private String mpn;

    private String gtin;

    private String title;

    private String minPrice;

    private String sample;

    private String otherSpec;

    private String url;

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the urlId
     */
    public String getUrlId() {
        return urlId;
    }

    /**
     * @param urlId the urlId to set
     */
    public void setUrlId(String urlId) {
        this.urlId = urlId;
    }

    /**
     * @return the brand
     */
    public String getBrand() {
        return brand;
    }

    /**
     * @param brand the brand to set
     */
    public void setBrand(String brand) {
        this.brand = brand;
    }

    /**
     * @return the mpn
     */
    public String getMpn() {
        return mpn;
    }

    /**
     * @param mpn the mpn to set
     */
    public void setMpn(String mpn) {
        this.mpn = mpn;
    }

    /**
     * @return the gtin
     */
    public String getGtin() {
        return gtin;
    }

    /**
     * @param gtin the gtin to set
     */
    public void setGtin(String gtin) {
        this.gtin = gtin;
    }

    /**
     * @return the title
     */
    public String getTitle() {
        return title;
    }

    /**
     * @param title the title to set
     */
    public void setTitle(String title) {
        this.title = title;
    }

    /**
     * @return the minPrice
     */
    public String getMinPrice() {
        return minPrice;
    }

    /**
     * @param minPrice the minPrice to set
     */
    public void setMinPrice(String minPrice) {
        this.minPrice = minPrice;
    }

    /**
     * @return the sample
     */
    public String getSample() {
        return sample;
    }

    /**
     * @param sample the sample to set
     */
    public void setSample(String sample) {
        this.sample = sample;
    }

    /**
     * @return the otherSpec
     */
    public String getOtherSpec() {
        return otherSpec;
    }

    /**
     * @param otherSpec the otherSpec to set
     */
    public void setOtherSpec(String otherSpec) {
        this.otherSpec = otherSpec;
    }

    /**
     * @return the url
     */
    public String getUrl() {
        return url;
    }

    /**
     * @param url the url to set
     */
    public void setUrl(String url) {
        this.url = url;
    }

}
